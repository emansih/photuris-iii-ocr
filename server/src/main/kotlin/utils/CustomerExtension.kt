package utils

import models.Customer

fun Customer.isUserSubscribed(): Boolean{
    return this.endTime != 0L
}

fun Customer.isUserFromGooglePlay(): Boolean {
    return this.paymentSource.contentEquals("googlePlay")
}

fun Customer.isUserFromStripe(): Boolean {
    return this.paymentSource.contentEquals("stripe")
}